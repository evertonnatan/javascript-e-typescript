// "i" vem de "index"
for (let i = 0; i <= 5; i++) {
    console.log(`Linh ${i}`);
}

// "for" negativo
for (let i = 10; i >= 5; i--) {
    console.log(`Linh ${i}`);
}

const frutas = ["Maçã", "Pêra", "Uva", "Banana", "Abacaxi", "Mamão", "Laranja"];

for (let i = 0; i < frutas.length; i++) {
    console.log(`Índice ${i}`, frutas[i]);
}